#include <iostream>

//比较运算符
int main() {
    int a=10;
    int b=30;
    //==
    //!=
    //>
    //<
    //>=
    //<=
    std::cout<<(a==b)<<std::endl;
    std::cout<<(a!=b)<<std::endl;
    std::cout<<(a>b)<<std::endl;
    std::cout<<(a<b)<<std::endl;
    std::cout<<(a>=b)<<std::endl;
    std::cout<<(a<=b)<<std::endl;
    std::cout << "Hello, World!" << std::endl;
    return 0;
}
